	private function data($version,$ecc,$dataCodeNumber,$eccNumber,$eccBlocks_1,$eccBlocks_2)
	{
		$this->versionErrorCorrectionCode[$version][$ecc] = [
			'DataCodeNumber'				=>$dataCodeNumber,	//数据码总数
			'ErrorCorrectingCodeNumber'		=>$eccNumber,		//每个块的纠错码字数
			'ErrorCorrectingCodeBlocks_1'	=>$eccBlocks_1,		//第一块的纠错代码
			'ErrorCorrectingCodeBlocks_2'	=>$eccBlocks_2,		//第二块的纠错代码
		];
	}
	
	//数据码总数
	public function getDataCodeNumber($version,$ecc)
	{
		return $this->versionErrorCorrectionCode[$version][$ecc]['DataCodeNumber'] * 8;
	}
	//纠错码字数
	public function getErrorCorrectingCodeNumber($version,$ecc)
	{
		return $this->versionErrorCorrectionCode[$version][$ecc]['ErrorCorrectingCodeNumber'];
	}
