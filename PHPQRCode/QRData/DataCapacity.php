	public function getVersion($length,$errorCorrectCode,$type = 'Number')
	{
		$this->init();
		foreach($this->DataCapacity as $key => $value)
		{
			if(!isset($value[$type]))
			{
				continue;
			}
			if($value[$type][$errorCorrectCode] - $length >= 0)
			{
